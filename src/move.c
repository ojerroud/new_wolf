#include "wolf3d.h"

void	move_up(t_env *e)
{
	e->map.width += 0;
}

void	move_down(t_env *e)
{
	e->map.width += 0;
}

void	move_right(t_env *e)
{
	e->map.width += 0;
}

void	move_left(t_env *e)
{
	e->map.width += 0;
}